package mvboard;

public class Mvboard {

	private int mvboardID;
	private String mvboardTitle;
	private String mvboardDate;
	private String mvboardContent;
	private int mvboardCnt;
	
	public int getMvboardID() {
		return mvboardID;
	}
	public void setMvboardID(int mvboardID) {
		this.mvboardID = mvboardID;
	}
	public String getMvboardTitle() {
		return mvboardTitle;
	}
	public void setMvboardTitle(String mvboardTitle) {
		this.mvboardTitle = mvboardTitle;
	}
	public String getMvboardDate() {
		return mvboardDate;
	}
	public void setMvboardDate(String mvboardDate) {
		this.mvboardDate = mvboardDate;
	}
	public String getMvboardContent() {
		return mvboardContent;
	}
	public void setMvboardContent(String mvboardContent) {
		this.mvboardContent = mvboardContent;
	}
	public int getMvboardCnt() {
		return mvboardCnt;
	}
	public void setMvboardCnt(int mvboardCnt) {
		this.mvboardCnt = mvboardCnt;
	}
}
